package chapter2;

public class ConditionalSentenceEx01 {

	public static void main(String[] args) {
		System.out.println("Code starts");
		
		if(true) {
			System.out.println("이 if문은 ");
			System.out.println("참이므로");
			System.out.println("코드가 실행됩니다.");
			
		} // end if
		
		if(false) {
			System.out.println("이 if문은 ");
			System.out.println("거짓이므로");
			System.out.println("코드가 실행되지 않습니다.");
			
		} // end if
		
		System.out.println("Cod ends");
	} // end main

}
