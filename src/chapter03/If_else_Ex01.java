package chapter03;

public class If_else_Ex01 {

	public static void main(String[] args) {
		System.out.println("Code starts");
		
		int num = 0;
		if(num == 0) {
			System.out.println("변수 num에 들어있는 값은 0입니다.");
		}else {
			System.out.println("변수 num에 들어있는 값은 0이 아닙니다.");
		}
		
		System.out.println("Code ends");
	} // end main

}
