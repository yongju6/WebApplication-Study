package chapter01;

public class SubstitutionCalculatorEx01 {

	public static void main(String[] args) {
		int num1 = 10;
		
		//num1 = num1 + 5;
		num1 += 5;
		
		//num1 = num1 - 5;
		num1 -= 5;
		
		//num1 = num1 * 5;
		num1 *= 5;
		
		//num1 = num1 / 5;
		num1 /= 5;
		
		//num1 = num1 % 5;
		num1 %= 5;
		
	}

}
