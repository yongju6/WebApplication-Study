package chapter01;

public class DataytypeChangeEx02 {

	public static void main(String[] args) {
		byte value1 = 127;
		System.out.println((int)value1);
		
		int value2 = 32768;
		System.out.println((short)value2);
		
		float value3 = 95.7f;
		System.out.println((double)value3);
		
		double value4 = 179.8;
		System.out.println((float)value4);
	}

}
