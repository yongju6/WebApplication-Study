package chapter11;

public class SimpleType {
	public int num1; 
	int num2;
	private int num3;
	
	public void func1() {
		System.out.println("func1");
	}
	
	void func2() {
		System.out.println("func2");
	}
	
	private void func3() {
		System.out.println("func3");
	}
	
	static void hello() {
		System.out.println("Hello World~!");
	}
}
